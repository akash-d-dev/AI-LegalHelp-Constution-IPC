def get_llm():
    """Lazy initialization of LLM."""
    global _llm
    if _llm is None:
        logger.info("ğŸ”„ Initializing LLM...")
        _llm = ChatOpenAI(temperature=0, api_key=Constants.OPENAI_API_KEY)
        logger.info("âœ… LLM initialized successfully")
    return _llm


